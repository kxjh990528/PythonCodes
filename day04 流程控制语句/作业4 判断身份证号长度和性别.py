"""
身份证号码的倒数第二位数字是奇数，表示性别为男性，否则为女性，引导用户输入身份证号，先判断是否为18位，再判断性别。
"""

# 获取用户输入
id_num = input("请输入身份证号码：")

# 判断身份证号码是否为18位
if len(id_num) != 18:
    print("身份证长度不对，请重新输入！")
else:  # 判断倒数第二位数字的奇偶来判断性别
    # 获取倒数第二位的数字
    second_last_digit = int(id_num[-2])
    # 判断性别
    if second_last_digit % 2 == 1:
        print("性别：男性")
    else:
        print("性别：女性")
